package afternoon.variable;

public class Var1 {
    public static void main(String[] args) {
        int a = 10;
        double b = 4.3;
        boolean c = true;
        char d = 'a';
        String e = "abc";
        float f = 3.14f;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }

}
